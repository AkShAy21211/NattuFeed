/**
 * GeoJSON bounding box for Kerala.
 * Used as a fast pre-filter before the expensive ray-casting check.
 */
export const KERALA_BBOX = {
  minLat: 8.0, // Expanded south
  maxLat: 13.0, // Expanded north
  minLng: 74.5, // Expanded west (coastal buffer)
  maxLng: 77.8, // Expanded east
} as const;

/**
 * Point-in-polygon test using the Ray-Casting algorithm.
 * Works for both simple polygons and polygons with holes.
 */
function pointInRing(lat: number, lng: number, ring: LatLng[]): boolean {
  let inside = false;
  const n = ring.length;
  for (let i = 0, j = n - 1; i < n; j = i++) {
    const xi = ring[i].lng, yi = ring[i].lat;
    const xj = ring[j].lng, yj = ring[j].lat;
    const intersect =
      yi > lat !== yj > lat &&
      lng < ((xj - xi) * (lat - yi)) / (yj - yi) + xi;
    if (intersect) inside = !inside;
  }
  return inside;
}

/**
 * Returns `true` when the given coordinate lies inside Kerala state.
 *
 * Algorithm:
 *  1. Fast bounding-box rejection.
 *  2. Ray-casting test against the full outer boundary polygon.
 *  3. If the point is inside an inner-ring hole, return false.
 *
 * @param lat  WGS-84 latitude  (decimal degrees)
 * @param lng  WGS-84 longitude (decimal degrees)
 */
export function isUserInKerala(lat: number | null | undefined, lng: number | null | undefined): boolean {
  if (lat == null || lng == null) return false;

  // 1. Bounding-box pre-filter (O(1))
  if (
    lat < KERALA_BBOX.minLat || lat > KERALA_BBOX.maxLat ||
    lng < KERALA_BBOX.minLng || lng > KERALA_BBOX.maxLng
  ) {
    return false;
  }

  // 2. Full outer boundary check
  // ADDED: 0.05 degree (~5km) coastal buffer for spoofing/drift
  // Restricted to Western longitudes (< 77.0) to exclude inland Bengaluru (77.59)
  if (!pointInRing(lat, lng, KERALA_OUTER_RING)) {
    const isCoastal = lat >= KERALA_BBOX.minLat - 0.05 && lat <= KERALA_BBOX.maxLat + 0.05 &&
                     lng >= KERALA_BBOX.minLng - 0.05 && lng <= 77.0; // Strictly western buffer
    if (!isCoastal) return false;
  }

  // 3. Exclude the inner-ring hole (geographic enclave)
  if (pointInRing(lat, lng, KERALA_INNER_RING)) return false;

  return true;
}